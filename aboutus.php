                                        <p style="text-align: center;">
                                        Сегодня космические технологии являются одной из наиболее наукоёмких и инновационных сфер экономики развитых стран. Анализ тенденций коммерческого применения космических технологий показывает, что одним из наиболее перспективных путей является создание и применение малых аппаратов и систем на их основе. Такие спутники разрабатываются во всех известных мировых университетах и коммерческих организациях. Миссией нашей компании является разработка и создание инновационных образовательных продуктов в области космических технологий, интернета вещей (IoT) и цифровых лабораторий. 
                                        </p>

                                        <h3 style="text-align: center;">История компании</h3>
                                        <ul>
                                            <li>2013: Под руководством ректора Г.Мутанова был выигран проект МОН РК, в рамках которого пять докторантов и магистрантов проходили стажировку на базе Токийского университета по международному проекту UNIFORM, получили необходимые знания и навыки создания малых космических аппаратов 
                                            <li>2015: Под руководством ректора Г.Мутанова выиграно Программно-целевое финансирование (ПЦФ), целью которого являлся запуск наноспутника в космос. Следует отметить, что в рамках реализации данного проекта наша страна впервые стала головным разработчиком космического аппарата 
                                            <li>2017: Запуск первого университетского наноспутника страны. Спутник <b>al-Farabi-1</b> был назван в честь выдающегося учёного тюркского мира, имя которого достойно носит наш университет
                                            <li>2018: Запуск второго университетского наноспутника <b>al-Farabi-2</b> на солнечно-синхронную орбиту 
                                            <li>2018: Под руководством директора Технопарка А.Темирбаева выигран проект на коммерциализацию результатов НИР по линии АО <b>Фонд Науки</b>
                                            <li>2019: Выпуск образовательных конструкторов малых космических аппаратов для школ
                                            
