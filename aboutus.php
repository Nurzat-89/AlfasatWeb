<h3 style="text-align: center;margin-top:0px;">Our Story</h3>
<p style="text-align: center;">Because Learning helps students and educators learn STEM through exciting, hands-on lessons. Our experiments and hardware kits give aspiring scientists real-world experience measuring and analyzing data, helping them learn how science, technology, engineering, and math work in the world around us. Our mission? To inspire students to pursue STEM careers and develop critical 21st century skills.</p>

<h3 style="text-align: center;">Company History</h3>
<ul>
<li>2014: Launched as Ardusat, our company gave students the ability to collect data from anywhere in the world via cubesats through our partner, Spire Global. Students loved working with real space data from satellites. Educators loved how we helped them bring STEM to life in the classroom.</li>
<li>2015:&nbsp;We raised a $1M seed round of funding to fuel product development and sales growth.</li>
<li>2016: We released eHub, an online learning platform to bring even more STEM experiences to students. With over 100 experiments available to students and educators, eHub quickly grew to be used in over 30 countries and in hundreds of schools across the world.</li>
<li>2017: Seeing an opportunity to inspire even more students beyond our space program, we rebranded the company to Because Learning. Because Learning provides sensor kits and online lessons for parents, students, and educators that incorporate science, engineering, coding, and math in addition to the Ardusat Space Program that lets students gather their own space data. Our overall mission remains the same: to bring hands-on STEM education to the next generation and inspire as many students as possible.</li>
</ul>
